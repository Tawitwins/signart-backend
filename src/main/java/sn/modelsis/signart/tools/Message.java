package sn.modelsis.signart.tools;

/**
 * Error message class
 *
 */
public class Message {

    String message;

    public Message(String message) {
        super();
        this.message = message;
    }

    public String getMessage() {
        return message;
    }

    public void setMessage(String message) {
        this.message = message;
    }

}
